public class DemoMath {
  public static void main(String[] args) {
    // pow
    // 2 pow 4 = 16
    double result = Math.pow(2.0, 4.0); // 16.0
    System.out.println(result);

    int x = 3;
    int y = 7;
    System.out.println(Math.abs(x - y)); //return an int, absolute difference)

    System.out.println(Math.round(9.45));
    System.out.println(Math.sqrt(81));

    System.out.println(Math.max(12, 9));
    System.out.println(Math.min(3, 4));



  }
}
